/** Replace `{{key}}` placeholders (e.g. `{{hubName}}`, `{{version}}`). */
export function fmtTemplate(template: string, vars: Record<string, string>): string {
  let s = template;
  for (const [k, v] of Object.entries(vars)) {
    s = s.split(`{{${k}}}`).join(v);
  }
  return s;
}

