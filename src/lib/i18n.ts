function detectLang(): Lang {
  // navigator.languages follows UI language list (not region).
  // First explicit "pl*" entry wins; otherwise EN.
  const langs = navigator.languages?.length ? navigator.languages : [navigator.language ?? "en"];
  const first = langs[0]?.toLowerCase() ?? "en";
  return first.startsWith("pl") ? "pl" : "en";
}

export const [lang, setLang] = createSignal<Lang>(detectLang());
export const t = () => translations[lang()];
